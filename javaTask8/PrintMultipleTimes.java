//Program 8: To print the Given String, multiple times
package javaTask8;

public class PrintMultipleTimes {
    public static void main(String[] args) {
        System.out.println("Printing the string 10 times: ");
        for (int i = 1; i <= 10; i++) {
            System.out.println("Welcome to Guvi");
        }
    }
}
